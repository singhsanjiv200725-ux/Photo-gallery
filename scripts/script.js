// ðŸŽµ Music tracks for each dress
const musicTracks = {
  dress1: "music/dress1.mp3",
  dress2: "music/dress2.mp3",
  dress3: "music/dress3.mp3",
  dress4: "music/dress4.mp3",
  dress5: "music/dress5.mp3",
  dress6: "music/dress6.mp3"
};

let currentSet = [];
let index = 0;
let slideInterval;
let audio = null;
let isMuted = false;

function openModal(setName) {
  currentSet = wardrobes[setName];
  index = 0;
  document.getElementById("myModal").style.display = "block";
  document.getElementById("modalImg").src = currentSet[index];

  document.body.classList.add("blurred");

  slideInterval = setInterval(() => {
    changeSlide(1);
  }, 1000);

  playMusic(setName);
}

function closeModal() {
  document.getElementById("myModal").style.display = "none";
  document.body.classList.remove("blurred");
  clearInterval(slideInterval);

  stopMusic();
}

function changeSlide(n) {
  if (currentSet.length === 0) return;
  index = (index + n + currentSet.length) % currentSet.length;
  document.getElementById("modalImg").src = currentSet[index];
}

// ðŸŽ¶ Handle music
function playMusic(setName) {
  stopMusic();
  if (musicTracks[setName]) {
    audio = new Audio(musicTracks[setName]);
    audio.loop = true;
    audio.play();
    isMuted = false;
    document.getElementById("musicToggle").textContent = "ðŸ’“";
  }
}

function stopMusic() {
  if (audio) {
    audio.pause();
    audio.currentTime = 0;
    audio = null;
  }
}

// ðŸ’“ toggle mute/unmute
document.getElementById("musicToggle").addEventListener("click", () => {
  if (!audio) return;
  isMuted = !isMuted;
  audio.muted = isMuted;
  document.getElementById("musicToggle").textContent = isMuted ? "ðŸ’”" : "ðŸ’“";
});

// ðŸ“¸ attach grid clicks
document.querySelectorAll("#grid a").forEach(link => {
  link.addEventListener("click", (e) => {
    e.preventDefault();
    let group = link.getAttribute("data-group");
    if (wardrobes[group]) {
      openModal(group);
    }
  });
});
